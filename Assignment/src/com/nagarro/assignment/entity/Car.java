package com.nagarro.assignment.entity;

public class Car {
	
	int carModel;
	String carType;
	int carCost;
	String insuranceType;
	double premiumInsurance;
	double basicInsurance;
	double totalInsurance;
	
	
	public int getCarModel() {
		return carModel;
	}
	public void setCarModel(int carModel) {
		this.carModel = carModel;
	}
	public String getCarType() {
		return carType;
	}
	public void setCarType(String carType) {
		this.carType = carType;
	}
	public int getCarCost() {
		return carCost;
	}
	public void setCarCost(int carCost) {
		this.carCost = carCost;
	}
	public String getInsuranceType() {
		return insuranceType;
	}
	public void setInsuranceType(String insuranceType) {
		this.insuranceType = insuranceType;
	}
	public double getPremiumInsurance() {
		return premiumInsurance;
	}
	public void setPremiumInsurance(double premiumInsurance) {
		this.premiumInsurance = premiumInsurance;
	}
	public double getBasicInsurance() {
		return basicInsurance;
	}
	public void setBasicInsurance(double basicInsurance) {
		this.basicInsurance = basicInsurance;
	}
	public double gettotalInsurance() {
		return totalInsurance;
	}
	public void settotalInsurance(double totalInsurance) {
		this.totalInsurance = totalInsurance;
	}
	
	
	
}
